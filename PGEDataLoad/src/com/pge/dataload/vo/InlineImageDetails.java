package com.pge.dataload.vo;

public class InlineImageDetails {

	private String tagContent=null;
	private String assetId=null;
	private String width=null;
	private String height=null;
	
	private String srcAttributeValue=null;
	private String imageType=null;
	private String imageName=null;
	private String imagePath=null;
	private String processingStatus=null;
	private String errorMessage=null;
	
	public String getSrcAttributeValue() {
		return srcAttributeValue;
	}
	public void setSrcAttributeValue(String srcAttributeValue) {
		this.srcAttributeValue = srcAttributeValue;
	}
	public String getImageType() {
		return imageType;
	}
	public void setImageType(String imageType) {
		this.imageType = imageType;
	}
	public String getTagContent() {
		return tagContent;
	}
	public void setTagContent(String tagContent) {
		this.tagContent = tagContent;
	}
	public String getAssetId() {
		return assetId;
	}
	public void setAssetId(String assetId) {
		this.assetId = assetId;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getImagePath() {
		return imagePath;
	}
	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}
	public String getProcessingStatus() {
		return processingStatus;
	}
	public void setProcessingStatus(String processingStatus) {
		this.processingStatus = processingStatus;
	}
	public String getErrorMessage() {
		return errorMessage;
	}
	public void setErrorMessage(String errorMessage) {
		this.errorMessage = errorMessage;
	}
	
	
	
	
}
